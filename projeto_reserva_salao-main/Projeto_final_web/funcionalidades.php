
<h4> Principais funcionalidades do aplicativo para reserva de salão de festas: </h4>
    <p> Agendamento Online - Permitindo que os moradores agendem o salão de festas diretamente pelo aplicativo, vizualizando datas disponíveis, horários e outras informações pertinentes.<p>
    <p> Calendário de Disponibilidade - Onde os moradores podem ver quais datas e horários estão ocupados ou disponíveis para a reserva.<p>
    <p> Regras e Políticas de Uso -  seção com as regras de uso do salão de festas, como número máximo de convidados, horários de silêncio, requisitos de limpeza, entre outros.<p>
    <p> Gestão Administrativa - A administração do condomínio tem um painel de controle onde monitora as reservas feitas e cumprimento das regras<P>
