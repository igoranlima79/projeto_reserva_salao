
<h4 style="font-family: Arial, sans-serif; font-size: 20px; color: #006400; text-align: center; margin-bottom: 20px;">
    Principais funcionalidades do aplicativo para reserva de salão de festas:
</h4>

<p style="font-family: Arial, sans-serif; font-size: 16px; line-height: 1.6; color: #000000; margin-bottom: 15px;">
    <strong>Agendamento Online:</strong> Permitindo que os moradores agendem o salão de festas diretamente pelo aplicativo, visualizando datas disponíveis, horários e outras informações pertinentes.
</p>

<p style="font-family: Arial, sans-serif; font-size: 16px; line-height: 1.6; color: #000000; margin-bottom: 15px;">
    <strong>Calendário de Disponibilidade:</strong> Onde os moradores podem ver quais datas e horários estão ocupados ou disponíveis para a reserva.
</p>

<p style="font-family: Arial, sans-serif; font-size: 16px; line-height: 1.6; color: #000000; margin-bottom: 15px;">
    <strong>Regras e Políticas de Uso:</strong> Seção com as regras de uso do salão de festas, como número máximo de convidados, horários de silêncio, requisitos de limpeza, entre outros.
</p>

<p style="font-family: Arial, sans-serif; font-size: 16px; line-height: 1.6; color: #000000; margin-bottom: 15px;">
    <strong>Gestão Administrativa:</strong> A administração do condomínio tem um painel de controle onde monitora as reservas feitas e cumprimento das regras.
</p>
