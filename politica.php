  <h2 style="font-size: 20px; color: #006600; text-align: center; margin-bottom: 15px;">Regras para Uso do Salão de Festas</h2>

  <p><strong>Reserva:</strong> O salão deve ser reservado na administração ou pelo sistema online. Reservas são confirmadas mediante pagamento da taxa (se aplicável).</p>
  
  <p><strong>Horário:</strong> Permitido entre 08:00 e 23:59, respeitando o horário de silêncio a partir das 22:00.</p>
  
  <p><strong>Responsabilidade:</strong> O responsável deve devolver o salão nas mesmas condições recebidas. Danos serão cobrados.</p>
  
  <p><strong>Limpeza:</strong> O salão deve ser deixado limpo após o uso. Caso contrário, será cobrada taxa de limpeza adicional.</p>
  
  <p><strong>Capacidade:</strong> Respeitar o limite de <em>[número máximo de pessoas]</em> para garantir segurança.</p>
  
  <p><strong>Som:</strong> Som alto é proibido, respeitando os horários de silêncio.</p>
  
  <p><strong>Decoração:</strong> Somente decorações previamente autorizadas. Não fixar itens nas paredes ou alterar a estrutura.</p>
  
  <p><strong>Bebidas e Fumo:</strong> Bebidas alcoólicas devem ser consumidas com responsabilidade. Fumar é proibido no salão.</p>
  
  <p><strong>Segurança:</strong> Listas de convidados podem ser exigidas. Apenas moradores e convidados autorizados têm acesso.</p>
  
  <p><strong>Sanções:</strong> Infrações podem resultar em multa ou restrição de uso, conforme o regimento interno.</p>
